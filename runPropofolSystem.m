% Declares system parameters and runs PropofolSystem.m
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

clear all
close all
clc

% choose parameters and bounds
% from Eleveld et al. 

    % deposition volumes [mL]
    V1 = 6.28 * 1000;               % plasma 
    V2 = 25.5 * 1000;               % fast periph.
    V3 = 273 * 1000;                % slow periph.

    % clearances [mL⁻¹ min⁻¹]
    CL = 1.79 * 1000;               % plasma
    Q2 = 1.75 * 1000;               % fast periph.
    Q3 = 1.11 * 1000;               % slow periph.

    % transport coefficients [min⁻¹]
    k10 = CL/V1;                    % plasma -> elimination
    k12 = Q2/V1;                    % fast periph. -> plasma
    k13 = Q3/V1;                    % slow periph. -> plasma
    k21 = Q2/V2;                    % plasma -> fast periph.
    k31 = Q3/V3;                    % plasma -> slow periph. 
    ke0 = 0.146;                    % plasma -> effect site

    % pharmacodynamic parameters
    ce50 = 3.08;                    % ce50 at 50% BIS [μg mL⁻¹]
    BIS0 = 93;                      % undrugged BIS [%]
    gamma = 1.47;                   % Hill cooperativity constant [-]

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

% set up controllers

    uCap = 5e4;                     % actuation limit, which is
                                    % around maintenance bolus for 
                                    % this patient; lower than induction
                                    % bolus [μg mL⁻¹ min⁻¹]
    BISmax = 60;                    % maximum safe BIS [%]
    BISmin = 40;                    % minimum safe BIS [%]
    BISdes = 50;                    % desired BIS [%]

% exponential controller

    a1 = sqrt(10); a2 = sqrt(10);   % eigenvalues         
    wExp = 5;                       % slack var. pareto weight for min

% graceful controller

    omega = 4; zeta = 1;        % natural freq., damping ratio
    wGrc1 = 1e8; wGrc2 = 1e2;       % slack var. pareto weights
                                    % for min, max
    ceMinGrc = -0.001;              % lowermost barrier  

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

% set up interval estimator

    d = 0.05;                       % disturbance magnitude bound
                                    % [μg mL⁻¹]
    V = 0.5;                        % sensor noise magnitude bound 
                                    % [μg mL⁻¹]
                                    % note that this corresponds to
                                    % about 10-20% BIS error points
    initError = 0.3;                % initial estimation error:
                                    % for the given graceful pareto 
                                    % weights, one state blows up at 0.5

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

% initialize patients
    
