% print per-IC summary metrics: induction time (first time BIS reaches
% the desired level), min/max BIS over the run, and total propofol dose
% administered (integral of the infusion rate over time)

function printPatientMetrics(patient0, patient0a, patient0b, ...
    patient1, patient2, patient3, patient4, patient5, patient6)
    labels = {'0', '0a', '0b', '1', '2', '3', '4', '5', '6'};
    patients = {patient0, patient0a, patient0b, ...
        patient1, patient2, patient3, patient4, patient5, patient6};
    fprintf('\n%-4s %16s %10s %10s %16s\n', ...
        'IC', 'Induction [min]', 'BISmin', 'BISmax', 'Total dose [ug]');
    for i = 1:length(patients)
        p = patients{i};
        inductionIdx = find(p.BISHist <= p.BISdes, 1, 'first');
        if isempty(inductionIdx)
            inductionTime = NaN;
        else
            inductionTime = p.tHist(inductionIdx);
        end
        totalDose = trapz(p.tHist, p.uHist);
        fprintf('%-4s %16.2f %10.2f %10.2f %16.1f\n', labels{i}, ...
            inductionTime, min(p.BISHist), max(p.BISHist), totalDose);
    end
    fprintf('\n');
end

