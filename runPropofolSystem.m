        fill([patient0.ceMin 20 20 patient0.ceMin], ...
            [-10 -10 10 10], [0.9 0.9 0.9], ...
            'EdgeColor', 'none', 'FaceAlpha', 0.6); 
        ceVals = linspace(-20, 20, 500);
        bound = -1 * patient0.a1 * (ceVals - patient0.ceMin);
        fill([ceVals fliplr(ceVals)], ...
