                k10, k12, k13, k21, k31, ke0, V1, ...
                BIS0, ce50, gamma, ...
                BISdes, BISmin, BISmax, ...
                uCap, a1, a2, wExp, ...
                ceMinGrc, zeta, omega, wGrc1, wGrc2, ...
                xLoEst0a, xHiEst0a, d, V);

    x0b = [patient0.ceMin-2; 0; 0; patient0.ceMin-initError]; 
    xLoEst0b = [x0b(1)-initError; 0; 0; x0b(4)-initError];
    xHiEst0b = [x0b(1)+initError; 0; 0; x0b(4)+initError];
    x0bColor = '#b7b7b7';
    patient0b = PropofolSystem(x0bColor, x0b, ...
                k10, k12, k13, k21, k31, ke0, V1, ...
                BIS0, ce50, gamma, ...
                BISdes, BISmin, BISmax, ...
                uCap, a1, a2, wExp, ...
                ceMinGrc, zeta, omega, wGrc1, wGrc2, ...
                xLoEst0b, xHiEst0b, d, V);

    x01 = [patient0.ceMax+initError; 0; 0; patient0.ceMax-initError]; 
    xLoEst01 = [x01(1)-initError; 0; 0; x01(4)-initError];
    xHiEst01 = [x01(1)+initError; 0; 0; x01(4)+initError];
    x1Color = '#66da54';
    patient1 = PropofolSystem(x1Color, x01, ...
                k10, k12, k13, k21, k31, ke0, V1, ...
                BIS0, ce50, gamma, ...
                BISdes, BISmin, BISmax, ...
                uCap, a1, a2, wExp, ...
                ceMinGrc, zeta, omega, wGrc1, wGrc2, ...
                xLoEst01, xHiEst01, d, V);

    x02 = [patient0.ceMax-1; 0; 0; patient0.ceMax-initError]; 
    xLoEst02 = [x02(1)-initError; 0; 0; x02(4)-initError];
    xHiEst02 = [x02(1)+initError; 0; 0; x02(4)+initError];
    x2Color = '#cada54';
    patient2 = PropofolSystem(x2Color, x02, ...
               k10, k12, k13, k21, k31, ke0, V1, ...
                BIS0, ce50, gamma, ...
                BISdes, BISmin, BISmax, ...
                uCap, a1, a2, wExp, ...
                ceMinGrc, zeta, omega, wGrc1, wGrc2, ...
                xLoEst02, xHiEst02, d, V);

    x03 = [patient0.ceMax; 0; 0; patient0.ceMax-0.7]; 
    xLoEst03 = [x03(1)-initError; 0; 0; x03(4)-initError];
    xHiEst03 = [x03(1)+initError; 0; 0; x03(4)+initError];
    x3Color = '#dcd62b';
    patient3 = PropofolSystem(x3Color, x03, ...
                k10, k12, k13, k21, k31, ke0, V1, ...
                BIS0, ce50, gamma, ...
                BISdes, BISmin, BISmax, ...
                uCap, a1, a2, wExp, ...
                ceMinGrc, zeta, omega, wGrc1, wGrc2, ...
                xLoEst03, xHiEst03, d, V);

    x04 = [patient0.ceDes-initError; 0; 0; patient0.ceDes-initError]; 
    xLoEst04 = [x04(1)-initError; 0; 0; x04(4)-initError];
    xHiEst04 = [x04(1)+initError; 0; 0; x04(4)+initError];
    x4Color = '#dda010';
    patient4 = PropofolSystem(x4Color, x04, ...
                k10, k12, k13, k21, k31, ke0, V1, ...
                BIS0, ce50, gamma, ...
