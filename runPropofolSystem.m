function [patient0, patient0a, patient0b, ...
        patient1, patient2, patient3, patient4, patient5, patient6] = ...
    simulatePatients(patient0, patient0a, patient0b, ...
        patient1, patient2, patient3, patient4, patient5, patient6, ...
        tStart, tEnd, nTimes, graceful, runObserver, useObserver)
    disp('Running Initial Condition 0...'); 
    patient0 = patient0.simulate(graceful, runObserver, useObserver, ...
        tStart, tEnd, nTimes);
    disp('Running Initial Condition 0a...'); 
    patient0a = patient0a.simulate(graceful, runObserver, useObserver, ...
        tStart, tEnd, nTimes);
    disp('Running Initial Condition 0b...'); 
    patient0b = patient0b.simulate(graceful, runObserver, useObserver, ...
        tStart, tEnd, nTimes);
    disp('Running Initial Condition 1...'); 
    patient1 = patient1.simulate(graceful, runObserver, useObserver, ...
        tStart, tEnd, nTimes);
    disp('Running Initial Condition 2...'); 
    patient2 = patient2.simulate(graceful, runObserver, useObserver, ...
        tStart, tEnd, nTimes);
    disp('Running Initial Condition 3...'); 
    patient3 = patient3.simulate(graceful, runObserver, useObserver, ...
        tStart, tEnd, nTimes);
    disp('Running Initial Condition 4...'); 
    patient4 = patient4.simulate(graceful, runObserver, useObserver, ...
        tStart, tEnd, nTimes);
    disp('Running Initial Condition 5...'); 
    patient5 = patient5.simulate(graceful, runObserver, useObserver, ...
        tStart, tEnd, nTimes);
    disp('Running Initial Condition 6...'); 
    patient6 = patient6.simulate(graceful, runObserver, useObserver, ...
        tStart, tEnd, nTimes);
    fprintf('\n'); 
end

