                ceMinGrc, zeta, omega, wGrc1, wGrc2, ...
                xLoEst04, xHiEst04, d, V);

    x05 = [patient0.ceMin-1.85; 0; 0; patient0.ceMin+0.12]; 
    xLoEst05 = [x05(1)-initError; 0; 0; x05(4)-initError];
    xHiEst05 = [x05(1)+initError; 0; 0; x05(4)+initError];
    x5Color = '#dd5510';
    patient5 = PropofolSystem(x5Color, x05, ...
                k10, k12, k13, k21, k31, ke0, V1, ...
